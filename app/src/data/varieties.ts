export function getVarietyById(id: string): AppleVariety | undefined {
  return varieties.find((v) => v.id === id);
}

export function getVarietiesByCategory(category: AppleVariety['category']): AppleVariety[] {
  return varieties.filter((v) => v.category === category);
}

export function getVarietiesByMonth(month: number): AppleVariety[] {
  return varieties.filter((v) => v.harvestMonth.includes(month));
}

